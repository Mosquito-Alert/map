export function spiderfyPoint (x, y, dist, sep, step) {
  let r = dist
  const b = sep / (2 * Math.PI)
  let phi = r / b

  for (let n = 0; n < step - 1; ++n) {
    phi += dist / r
    r = b * phi
  }
  x = x + r * Math.cos(phi)
  y = y + r * Math.sin(phi)

  // this.print = function () {
  //   console.log('Step ' + step + '  ' + this.x + ', ' + this.y)
  // }

  return [x, y]
}

export default spiderfyPoint
